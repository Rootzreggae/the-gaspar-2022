    <h1>Introduction</h1>
    <p>
        ELSA, which stands for
        <b>E</b>nglish
        <b>L</b>anguage
        <b>S</b>peech
        <b>A</b>ssistant, is a mobile app that uses AI to help people improve
        their pronunciation. Users can access different curriculum options based
        on their interests, such as travel or professional settings. They’re
        then lead through a series of vocabulary words and phrases, and ELSA's
        algorithms analyze their speech. If ELSA detects a mispronunciation, it
        tells the user not only how the word should sound, but also how to move
        the tongue and lips to form the correct word.
    </p>

    <p>
        With ELSA Speak you can work through a wide range of activities which
        mainly focus on the individual sounds of English. Most activities
        involve recording yourself saying a word or phrase. The phrases are
        topic related (introductions, family, business, food and drink, etc) so
        not only can you improve your pronunciation, you can also learn phrases
        that are relevant to your own context.
    </p>

