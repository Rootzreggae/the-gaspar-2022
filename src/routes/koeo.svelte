    <p>
        User persona is a method / tool created in product design and
        development process to represent the characters of a group pf people. It
        answers the questions like “For whom i'm designing the product?” and
        “What are the characters of this group of people?” which enable the
        product made to meet the real needs of its users. User personas also
        make the product design process easier by offering a vivid character of
        users. Based on the data gathered the persona below was developed.
