    <h1>case study: DESIGNING A FOOTBALL STREAMING APP FROM THE GROUND UP</h1>
    <br />

    <p>
        Keep One Eye Open (Koeo) is a premium Stream & Search app that allows
        you to watch football events while also interacting with players,
        coaches, and other football fans.
    </p>
    <p>
       
The greatest benefit Koeo gives serious football fans is the affordability and freedom of a monthly subscription.
    </p>

    <h3>Goals</h3>
    <p>
        Koeo aims to close the gap between fans and players, providing
        interaction during a match stream, and a greater sense of virtual
        community.
    </p>
    <p>
        Football entities such as players and coaches are verified and are
        automatically granted mod access. This ensures control for the
        professionals and behavioural responsabilities for the fans
    </p>
    <p>
        The overall goal of this project is to showcase that as a Designer i am
        fluid, and i can adapt with the work at hand. This includes interface
        design, user research, illustration and front-end coding (this website).
    </p>

    <h3>Ideation & validation</h3>
    <p>
        This project was born from failure. A failed challenge for a company i
        have applied. Despite not getting the position, i really liked the idea
        and as such, i took the motive and opportunity to build something that i
        believe in.
    </p>
    <p>
        I had the general idea of the product down, but i had a million
        questions and like...three answers!
    </p>
    <h3>The process</h3>

    <p>I sought to answer the main questions i had:</p>
    <ul>
        <li>
            <b>The problem: </b> is this problem worth solving? Do users think this
            is a major problem? Is my solution appealing?
        </li>
        <li>
            <b>The market:</b> Some users might agree that this is a problem worth
            solving. But are there enough of them to make up a market for my product?
        </li>
        <li>
            <b>The product:</b> The problem might exist, but does my product actually
            solve it?
        </li>
        <li>
            <b>Willingness to pay:</b> There is market demand and a great product
            idea. But will people actually be willing to reach into their wallets
            and pay for it?
        </li>
    </ul>

    <h3>Conducting user research</h3>

    <p>
        I was fairly confident in the product idea, but i first needed to figure
        out whether the problem is a real one that needs solving. To do this, my
        best option was to talk to potential users directly. The focus here is
        on getting qualitative validation of the product idea.
    </p>
    <p>
        I found five people (complete strangers to me, 3 Uber drivers and 2
        random people) who said they would be keen to use my hypothetical
        product. While it doesn’t necessarily guarantee that the problem
        actually exists or that my proposed solution is good or valuable, it was
        the simplest way to begin validating the problem i’ve identified.
    </p>

    <p>
        To broaden my research, i've made a survey and posted on Reddit
        Portugal. With the survey i gathered quantitative data to build my
        persona.
